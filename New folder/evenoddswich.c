#include<stdio.h>
int main()
{
	int n;
	printf("\n enter any number 0 to 10");
	scanf("%d",&n);
	switch(n)
	{
		case 1:
			case 3:
				case 5:
					case 7:
						case 9:
							printf("\n odd");
							break;
							case 2:
								case 4:
									case 6:
										case 8:
											case 10:
												printf("\n even");
												break;
												default:
													printf("\n invalid input");
													break;
	}
}
